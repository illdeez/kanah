export interface StoryContent {
  id: number;
  storyNumber: number;
  title: string;
  readingTime: string;
  story: string;
  hiddenMeaning: string;
  lifeImpact: string;
  reflectionQuestion: string;
  dailyAction: string;
  selectedLines: string[];
  pledgeText: string;
  contentReady: boolean;
}

export interface WordTrack {
  id: string;
  type?: "wordTrack";
  word: string;
  subtitle: string;
  description: string;
  totalStories: number;
  stories: StoryContent[];
}

export interface NameItem {
  id: string;
  number: number;
  name: string;
  title: string;
  readingTime: string;
  story: string;
  hiddenMeaning: string;
  lifeImpact: string;
  reflectionQuestion: string;
  dailyAction: string;
  selectedLines: string[];
  pledgeText: string;
  contentReady: boolean;
  stories?: StoryContent[];
}

export interface NamesTrack {
  id: string;
  type: "namesTrack";
  word: string;
  subtitle: string;
  description: string;
  totalNames: number;
  names: NameItem[];
}

export type Track = WordTrack | NamesTrack;

export function isNamesTrack(track: Track): track is NamesTrack {
  return (track as NamesTrack).type === "namesTrack";
}

export function isWordTrack(track: Track): track is WordTrack {
  return !isNamesTrack(track);
}

